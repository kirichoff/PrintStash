#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_parse_duration() {
        assert_eq!(parse_duration("1h 23m 45s"), Some(5025));
        assert_eq!(parse_duration("2d 4h"), Some(187200));
        assert_eq!(parse_duration("90s"), Some(90));
        assert_eq!(parse_duration("90"), Some(90));
        assert_eq!(parse_duration("3.5"), Some(3));
        assert_eq!(parse_duration(""), None);
    }

    #[test]
    fn test_derive_slicer_name() {
        assert_eq!(derive_slicer_name("OrcaSlicer 2.3.0"), Some("OrcaSlicer"));
        assert_eq!(derive_slicer_name("PrusaSlicer-2.9.0"), Some("PrusaSlicer"));
        assert_eq!(
            derive_slicer_name("BambuStudio 1.10"),
            Some("BambuStudio")
        );
        assert_eq!(derive_slicer_name(""), None);
    }

    #[test]
    fn test_metadata_pattern_match() {
        let text = "; PrusaSlicer 2.9.0 on Linux\n\
                    ; printer_model = MK4\n\
                    ; nozzle_diameter = 0.4\n\
                    ; layer_height = 0.2\n\
                    ; sparse_infill_density = 15%\n\
                    ; estimated printing time (normal mode) = 1h 30m 0s\n\
                    ; total filament used [g] = 45.3\n\
                    ; total filament length [mm] = 15200.5\n\
                    ; total filament cost = 3.20\n\
                    ; filament_type = PLA\n";
        let meta = apply_patterns(text);
        assert_eq!(meta.slicer_name.as_deref(), Some("PrusaSlicer"));
        assert_eq!(meta.printer_model.as_deref(), Some("MK4"));
        assert_eq!(meta.nozzle_diameter_mm, Some(0.4));
        assert_eq!(meta.layer_height_mm, Some(0.2));
        assert_eq!(meta.infill_percent, Some(15.0));
        assert_eq!(meta.estimated_time_s, Some(5400));
        assert_eq!(meta.filament_weight_g, Some(45.3));
        assert_eq!(meta.filament_length_mm, Some(15200.5));
        assert_eq!(meta.filament_cost, Some(3.2));
        assert_eq!(meta.material_type.as_deref(), Some("PLA"));
    }

    #[test]
    fn test_thumb_scanner_single_block() {
        let mut scanner = ThumbScanner::new();
        // A tiny 1-pixel PNG (simplified for test).
        let b64 = "iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAYAAAAfFcSJAAAADUlEQVR42mP8/5+hHgAHggJ/PchI7wAAAABJRU5ErkJggg==";
        scanner.feed(&format!("; thumbnail begin 1x1 {}\n; {b64}\n; thumbnail end\n", b64.len()));
        let result = scanner.finish().unwrap();
        assert!(result.len() > 10); // reasonable PNG header
    }

    #[test]
    fn test_thumb_scanner_picks_largest() {
        let mut scanner = ThumbScanner::new();
        let small_b64 = "iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAYAAAAfFcSJAAAADUlEQVR42mP8/5+hHgAHggJ/PchI7wAAAABJRU5ErkJggg==";
        let large_b64 = "iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAYAAAAfFcSJAAAADUlEQVR42mP8/5+hHgAHggJ/PchI7wAAAABJRU5ErkJggg==";
        scanner.feed(&format!(
            "; thumbnail begin 100x100 {}\n; {small_b64}\n; thumbnail end\n",
            small_b64.len()
        ));
        scanner.feed(&format!(
            "; thumbnail begin 200x200 {}\n; {large_b64}\n; thumbnail end\n",
            large_b64.len()
        ));
        let result = scanner.finish().unwrap();
        assert_eq!(result.len(), 69); // 200x200 is larger, so decoded block = 69 bytes
    }
}
