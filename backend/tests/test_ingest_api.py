def _large_gcode(min_bytes: int = 1_200_000) -> bytes:
    payload = b"; generated by OrcaSlicer\nG90\nG1 X1 Y1 E1\n"
    payload += b"; padding to exceed nginx default body limit\n" * (
        min_bytes // 45
    )
    return payload


def _large_cube_stl(min_bytes: int = 1_200_000) -> bytes:
    lines = _cube_stl().splitlines()
    body = b"\n".join(lines[1:-1]) + b"\n"
    repeat_count = min_bytes // len(body) + 1
    return b"solid large-cube\n" + body * repeat_count + b"endsolid large-cube\n"


def _configure_storage(tmp_path: Path) -> None:
    _overlay["data_dir"] = tmp_path / "files"
    _overlay["thumb_dir"] = tmp_path / "thumbs"
    _overlay["staging_dir"] = tmp_path / "staging"


def _completed_job(client: TestClient, response) -> dict:
    assert response.status_code == 202, response.text
    job_id = response.json()["job_id"]
    headers = {}
    auth = response.request.headers.get("authorization")
    if auth:
        headers["Authorization"] = auth
    job = client.get(f"/api/v1/ingest/jobs/{job_id}", headers=headers)
    assert job.status_code == 200, job.text
    payload = job.json()
    assert payload["state"] == "completed", payload
    assert payload["model_id"] is not None
    assert payload["file_id"] is not None
    return payload


def _assert_file_created(session: Session, file_id: int, file_type: FileType) -> File:
    file_row = session.get(File, file_id)
    assert file_row is not None
    assert file_row.file_type == file_type
    assert file_row.size_bytes > 0
    assert get_backend().exists(file_row.path)
    metadata = session.exec(select(Metadata).where(Metadata.file_id == file_id)).one()
    assert metadata.file_id == file_id
    model = session.get(Model, file_row.model_id)
    assert model is not None
    assert model.thumbnail_file_id == file_id
    assert model.thumbnail_path
    assert get_backend().exists(model.thumbnail_path)
    return file_row


def test_ingest_orca_gcode_creates_db_blob_and_thumbnail(
    tmp_path: Path,
    client: TestClient,
    db_session: Session,
    auth_headers: dict[str, str],
) -> None:
    _configure_storage(tmp_path)
    gcode = (Path(__file__).parent / "fixtures" / "sample.gcode").read_bytes()

    payload = _completed_job(
        client,
        client.post(
            "/api/v1/ingest/orca",
            headers=auth_headers,
            files={"file": ("sample.gcode", gcode, "text/plain")},
            data={"model_name": "Sample G-code"},
        ),
    )
    file_id = payload["file_id"]
    _assert_file_created(db_session, file_id, FileType.GCODE)

    download = client.get(f"/api/v1/files/{file_id}/download", headers=auth_headers)
    assert download.status_code == 200, download.text
    assert b"; generated by OrcaSlicer" in download.content

    thumbnail = client.get(f"/api/v1/files/{file_id}/thumbnail", headers=auth_headers)
    assert thumbnail.status_code == 200, thumbnail.text
    assert thumbnail.headers["content-type"] == "image/webp"
    assert thumbnail.content.startswith(WEBP_MAGIC)

    filament_profile = db_session.exec(
        select(FilamentProfile).where(FilamentProfile.name == "Generic PLA")
    ).one()
    assert filament_profile.material_type == "PLA"
    assert filament_profile.cost_per_kg == 28

    printer_profile = db_session.exec(
        select(PrinterProfile).where(PrinterProfile.name == "Ender-3 V3 SE")
    ).one()
    assert printer_profile.slicer_name == "OrcaSlicer"
    assert printer_profile.nozzle_diameter_mm == 0.4


