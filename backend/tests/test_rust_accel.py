"""Tests for Rust acceleration wrappers and their Python fallback paths.

These tests pass whether or not the ``_nexus3d_rust`` native module is built.
"""

from __future__ import annotations

import struct
from pathlib import Path


# ---------------------------------------------------------------------------
# gcode_rust — wrapper + fallback
# ---------------------------------------------------------------------------


class TestGcodeRust:
    def test_wrapper_imports_without_native(self):
        from app.services import gcode_rust

        assert gcode_rust._rust_scan is None  # native module not built

    def test_gcode_scan_returns_none_when_native_missing(self, tmp_path: Path):
        from app.services import gcode_rust

        path = tmp_path / "missing.gcode"
        result = gcode_rust.gcode_scan(path)
        assert result is None

    def test_gcode_scan_returns_dict_when_native_present(self, monkeypatch):
        """If the native module were available, this is what the return shape looks like."""
        expected = {
            "sha256": "abc123",
            "slicer_name": "OrcaSlicer",
            "slicer_version": "2.3.0",
            "printer_model": "MK4",
            "nozzle_diameter_mm": 0.4,
            "layer_height_mm": 0.2,
            "infill_percent": 15.0,
            "estimated_time_s": 5400,
            "filament_weight_g": 45.3,
            "filament_length_mm": 15200.5,
            "filament_cost": 3.2,
            "material_type": "PLA",
            "thumbnail_png": b"\x89PNG...",
        }

        def fake_scan(_path: str) -> dict:
            return expected

        monkeypatch.setattr("app.services.gcode_rust._rust_scan", fake_scan)
        from app.services import gcode_rust  # re-import with monkeypatched

        result = gcode_rust.gcode_scan(Path("fake.gcode"))
        assert result is not None
        assert result["sha256"] == "abc123"
        assert result["slicer_name"] == "OrcaSlicer"


